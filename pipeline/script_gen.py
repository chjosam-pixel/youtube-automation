import json

from openai import OpenAI

from pipeline.config import OPENAI_API_KEY, SCRIPT_MODEL

client = OpenAI(api_key=OPENAI_API_KEY)

SYSTEM_PROMPT = """أنت كاتب سيناريو وثائقي متمرس، متخصص في إعداد سيناريوهات وثائقية إخبارية بالعربية الفصحى (MSA) لقناة يوتيوب تغطي أهم المواضيع والأحداث الرائجة يوميًا حول العالم (أخبار، ظواهر اجتماعية، رياضة، شخصيات، تقنية، ثقافة، إلخ).
أسلوبك في الكتابة هادئ وموضوعي وغني بالتفاصيل البصرية، ونغمة السرد هي صوت مذيع وثائقي رزين ودقيق. يجب أن يتناول السيناريو الموضوع المعطى كما هو فعليًا (سياقه الحالي وأهميته وأبعاده)، دون افتراض أنه موضوع تاريخي إلا إذا كان الموضوع نفسه تاريخيًا بالفعل.

يجب أن يكون الناتج بصيغة JSON التالية بالضبط، ولا تكتب أي نص إضافي خارج الـ JSON:
{
  "title": "عنوان جذاب للفيديو باللغة العربية الفصحى (لا يتجاوز 100 حرف، يحتوي كلمات مفتاحية لتحسين البحث)",
  "description": "وصف فيديو يوتيوب بالعربية الفصحى (150-300 كلمة، يتضمن ملخص الموضوع و3-5 إشارات لوسوم ذات صلة)",
  "tags": ["وسم1", "وسم2", "... من 8 إلى 15 وسمًا بالعربية و/أو الإنجليزية"],
  "scenes": [
    {
      "narration": "نص السرد لهذا المشهد بالعربية الفصحى (فقرة من 5-8 جمل، مناسبة للقراءة الصوتية، بنغمة هادئة وموضوعية)",
      "image_prompt": "وصف بالإنجليزية فقط يُستخدم لتوليد صورة بالذكاء الاصطناعي، يصف المشهد بما يناسب الموضوع الفعلي، بأسلوب cinematic documentary illustration, painterly, dramatic lighting, no text, no watermark"
    }
  ]
}

المتطلبات (يجب الالتزام بها بشكل صارم، هذه شروط إلزامية لا اقتراحات):
- عدد scenes يجب أن يكون 12 بالضبط.
- يجب أن يكون عدد كلمات narration في كل مشهد بين 110 و150 كلمة عربية، ولا يجوز أبدًا أن يكون السرد عبارة عن جملة أو جملتين قصيرتين.
- لذلك يجب أن يبلغ إجمالي عدد كلمات narration في السيناريو كاملاً 1300 كلمة على الأقل (12 مشهدًا × حوالي 110 كلمة)، لكي تتراوح مدة القراءة الصوتية الإجمالية بين 5 و10 دقائق. نقص عدد الكلمات خطأ غير مقبول.
- يجب أن يحتوي كل مشهد على تفاصيل محددة وحقائق وأمثلة وسياق ملموس، وليس جملًا عامة فارغة، لملء المساحة المطلوبة.
- يجب أن يكون المحتوى مستندًا إلى وقائع حقيقية ودقيقة عن الموضوع المعطى، وأن يكون للسرد بداية (مقدمة عن الموضوع وسياقه)، وتطور (التفاصيل والجوانب المختلفة، يمكن تقسيمها إلى عدة مشاهد)، ونهاية (الأهمية والتأثير وما يعنيه هذا الموضوع اليوم).
- تجنب أي تعبيرات سياسية حساسة معاصرة، وحافظ على منظور سرد تاريخي موضوعي ومحايد.
- استخدم العربية الفصحى المعيارية (Modern Standard Arabic) في title وdescription وtags وnarration فقط.
- يجب أن يكون image_prompt بالإنجليزية فقط، ولا يحتوي على أي نص أو ترجمة أو عناصر حديثة.
- قد يكون الموضوع المعطى كلمة أو عبارة مفتاحية رائجة قصيرة وغامضة. إذا تم تزويدك بعناوين أخبار حقيقية مرتبطة بهذا الموضوع، يجب أن تستند وقائع السيناريو إليها حصريًا.
- قاعدة صارمة لا استثناء فيها بأي حال: كل حقيقة محددة تذكرها (تاريخ، يوم، شهر، سنة، مكان، اسم شخص أو فريق أو منظمة، رقم، إحصائية، نتيجة مباراة، عدد ضحايا، إلخ) يجب أن تكون مذكورة صراحةً في العناوين/المصادر المزوّدة لك أو في "Trend detected on". يُمنع منعًا تامًا اختراع أو تخمين أو "تعبئة" أي حقيقة محددة غير موجودة في المصادر المعطاة، حتى لو بدت منطقية أو محتملة. الدقة الواقعية أهم من اكتمال التفاصيل.
- عندما تحتاج إلى تفصيل غير متوفر في المصادر (مثل تاريخ دقيق، أو رقم دقيق، أو اسم لم يُذكر)، استخدم صياغة عامة وغامضة عمدًا بدلاً من حقيقة مخترعة، مثل: "في الأيام الأخيرة" بدلاً من تاريخ محدد، "أحد المسؤولين" بدلاً من اسم غير مؤكد، "عدد من الأشخاص" بدلاً من رقم غير مؤكد. الغموض المتعمد مقبول دائمًا؛ الحقيقة المختلقة غير مقبولة أبدًا.
- إذا لم تُزوَّد بعناوين أخبار لهذا الموضوع، التزم بوصف عام للموضوع دون افتراض أي سياق محدد (نوع حدث، تاريخ، مكان، أطراف) لم يُذكر صريحًا في الموضوع نفسه.
"""

MIN_TOTAL_WORDS = 1100
MAX_ATTEMPTS = 3


def _total_narration_words(data: dict) -> int:
    return sum(len(s.get("narration", "").split()) for s in data.get("scenes", []))


def _request_script(messages: list[dict]) -> dict:
    response = client.chat.completions.create(
        model=SCRIPT_MODEL,
        messages=messages,
        response_format={"type": "json_object"},
        temperature=0.8,
    )
    return json.loads(response.choices[0].message.content)


def generate_script(topic: str, context: list[str] | None = None) -> dict:
    user_prompt = f"اكتب سيناريو وثائقيًا إخباريًا بالعربية الفصحى حول الموضوع الرائج حاليًا: «{topic}»."
    if context:
        headlines = "\n".join(f"- {line}" for line in context)
        user_prompt += (
            "\n\nعناوين أخبار حقيقية مرتبطة بهذا الموضوع (استخدمها كمصدر وقائعي وحيد، ولا تخرج عنها):\n"
            f"{headlines}"
        )
    messages = [
        {"role": "system", "content": SYSTEM_PROMPT},
        {"role": "user", "content": user_prompt},
    ]

    data = None
    for attempt in range(MAX_ATTEMPTS):
        data = _request_script(messages)
        total_words = _total_narration_words(data)
        if total_words >= MIN_TOTAL_WORDS:
            break
        messages.append({"role": "assistant", "content": json.dumps(data, ensure_ascii=False)})
        messages.append({
            "role": "user",
            "content": (
                f"عدد كلمات السرد الحالي هو {total_words} كلمة فقط، وهذا غير كافٍ على الإطلاق، "
                f"يجب أن يبلغ {MIN_TOTAL_WORDS} كلمة على الأقل. "
                f"أعد إخراج الـ JSON كاملاً مع توسيع narration في كل مشهد إلى 110-150 كلمة، "
                f"بإضافة تفاصيل تاريخية محددة وحوارات ووصف للمشاهد، دون حذف أو تقصير أي مشهد."
            ),
        })

    assert data is not None
    assert "scenes" in data and len(data["scenes"]) > 0, "Script generation returned no scenes"
    assert "title" in data and data["title"], "Script generation returned no title"

    return data


if __name__ == "__main__":
    import sys

    topic = sys.argv[1] if len(sys.argv) > 1 else "世宗大王与韩文的创制"
    script = generate_script(topic)
    print(json.dumps(script, ensure_ascii=False, indent=2))
