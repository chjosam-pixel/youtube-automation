import json

from openai import OpenAI

from pipeline.config import OPENAI_API_KEY, SCRIPT_MODEL

client = OpenAI(api_key=OPENAI_API_KEY)

SYSTEM_PROMPT = """أنت كاتب سيناريو وثائقي متمرس، متخصص في إعداد سيناريوهات وثائقية إخبارية بالعربية الفصحى (MSA) لقناة يوتيوب متخصصة تعمل ضمن ثلاثة محاور ثابتة فقط:
• الحوادث والكوارث (حوادث، جرائم، كوارث طبيعية، حرائق، انهيارات)
• الجيوسياسة والصراعات (حروب، انتخابات، دبلوماسية، أزمات دولية)
• الرياضة الكبرى (كرة القدم العالمية، الملاكمة، الفورمولا 1، التنس، الأولمبياد — باستثناء الكريكيت)
هذه المحاور الثلاثة هي هوية القناة الثابتة. كل سيناريو يجب أن يعكس أحد هذه المحاور بوضوح.
أسلوبك في الكتابة هادئ وموضوعي وغني بالتفاصيل البصرية، ونغمة السرد هي صوت مذيع وثائقي رزين ودقيق. يجب أن يتناول السيناريو الموضوع المعطى كما هو فعليًا (سياقه الحالي وأهميته وأبعاده)، دون افتراض أنه موضوع تاريخي إلا إذا كان الموضوع نفسه تاريخيًا بالفعل.

يجب أن يكون الناتج بصيغة JSON التالية بالضبط، ولا تكتب أي نص إضافي خارج الـ JSON:
{
  "title": "عنوان قصير ومباشر ومحسّن لمحركات البحث (Google/YouTube SEO)، بين 25 و50 حرفًا فقط، يبدأ بالكلمة المفتاحية الأهم للموضوع، بدون حشو أو وصف زائد",
  "description": "وصف فيديو يوتيوب بالعربية الفصحى (150-300 كلمة، يتضمن ملخص الموضوع و3-5 إشارات لوسوم ذات صلة)",
  "tags": ["وسم1", "وسم2", "... من 8 إلى 15 وسمًا بالعربية و/أو الإنجليزية"],
  "scenes": [
    {
      "narration": "نص السرد لهذا المشهد بالعربية الفصحى (جملة واحدة كاملة فقط، من 35 إلى 50 كلمة، مناسبة للقراءة الصوتية، بنغمة هادئة وموضوعية)",
      "image_prompt": "وصف بالإنجليزية فقط يُستخدم لتوليد صورة بالذكاء الاصطناعي، يصف المشهد بما يناسب الموضوع الفعلي، بأسلوب cinematic documentary illustration, painterly, dramatic lighting, no text, no watermark"
    }
  ]
}

المتطلبات (يجب الالتزام بها بشكل صارم، هذه شروط إلزامية لا اقتراحات):
- عدد scenes يجب أن يكون 8 بالضبط. كل مشهد يقابل صورة مختلفة، لذلك يجب أن يحمل كل مشهد فكرة أو زاوية مختلفة عن الموضوع حتى تتنوع الصور. يُستخدم هذا السيناريو لإنتاج فيديو رئيسي بتنسيق 16:9 مدته حوالي 3 دقائق، ومقطع Shorts بتنسيق 9:16 مدته حوالي دقيقة واحدة (يأخذ تلقائياً المشاهد الأولى حتى يصل إلى الدقيقة).
- هيكل المشاهد الثمانية إلزامي على النحو التالي:
  • المشهد 1 (Hook): افتتاحية صادمة أو سؤال مثير للفضول يجذب المشاهد فورًا ويجعله يريد معرفة المزيد — أسلوب "هل تعلم أن..." أو "ما الذي حدث فعلًا في..." أو إحصائية مذهلة.
  • المشهد 2 (Tease): تصعيد الفضول، تقديم أبعاد الحدث دون الكشف عن كل التفاصيل، مع الإيحاء بأن القصة أعمق مما يبدو — يُنتهى بنبرة تشويقية تدفع المشاهد لمتابعة الفيديو الكامل.
  • المشاهد 3-6 (Core): التفاصيل والأبعاد المختلفة للموضوع، كل مشهد زاوية مختلفة.
  • المشهد 7 (Insight): التحليل والأهمية الأوسع لهذا الحدث.
  • المشهد 8 (Conclusion + CTA): الخلاصة والتأثير، وتنتهي الجملة بعبارة طبيعية تدعو إلى مشاهدة المزيد مثل "تابع معنا لمعرفة كل التفاصيل" أو "القصة الكاملة في الفيديو".
- يجب أن يكون narration في كل مشهد جملة واحدة كاملة فقط (وليس جملتين أو أكثر)، طولها بين 35 و50 كلمة عربية.
- لذلك يجب أن يبلغ إجمالي عدد كلمات narration في السيناريو كاملاً بين 280 و400 كلمة (8 مشاهد × حوالي 35-50 كلمة)، لكي تكون مدة القراءة الصوتية الإجمالية للفيديو الرئيسي حوالي 3 دقائق (170-200 ثانية). الزيادة المفرطة عن هذا الحد خطأ غير مقبول بقدر النقص.
- يجب أن يحتوي كل مشهد على تفصيل محدد أو حقيقة أو مثال واحد ملموس ضمن جملته الواحدة، وليس جملة عامة فارغة.
- ممنوع منعًا تامًا أن يحتوي narration أي مشهد على أكثر من جملة واحدة (لا فاصلة طويلة مركّبة، ولا جملتين مفصولتين بنقطة).
- تجنب أي تعبيرات سياسية حساسة معاصرة، وحافظ على منظور سرد تاريخي موضوعي ومحايد.
- استخدم العربية الفصحى المعيارية (Modern Standard Arabic) في title وdescription وtags وnarration فقط.
- title يجب أن يكون قصيرًا جدًا وموجزًا (25-50 حرفًا)، يضع الكلمة المفتاحية الأهم في البداية مباشرة، بأسلوب عناوين الأخبار المختصرة على Google/YouTube، دون أي إطالة أو وصف فرعي أو فواصل غير ضرورية.
- يجب أن يكون image_prompt بالإنجليزية فقط، ولا يحتوي على أي نص أو ترجمة أو عناصر حديثة.
- قد يكون الموضوع المعطى كلمة أو عبارة مفتاحية رائجة قصيرة وغامضة. إذا تم تزويدك بعناوين أخبار حقيقية مرتبطة بهذا الموضوع، يجب أن تستند وقائع السيناريو إليها حصريًا.
- قاعدة صارمة لا استثناء فيها بأي حال: كل حقيقة محددة تذكرها (تاريخ، يوم، شهر، سنة، مكان، اسم شخص أو فريق أو منظمة، رقم، إحصائية، نتيجة مباراة، عدد ضحايا، إلخ) يجب أن تكون مذكورة صراحةً في العناوين/المصادر المزوّدة لك أو في "Trend detected on". يُمنع منعًا تامًا اختراع أو تخمين أو "تعبئة" أي حقيقة محددة غير موجودة في المصادر المعطاة، حتى لو بدت منطقية أو محتملة. الدقة الواقعية أهم من اكتمال التفاصيل.
- عندما تحتاج إلى تفصيل غير متوفر في المصادر (مثل تاريخ دقيق، أو رقم دقيق، أو اسم لم يُذكر)، استخدم صياغة عامة وغامضة عمدًا بدلاً من حقيقة مخترعة، مثل: "في الأيام الأخيرة" بدلاً من تاريخ محدد، "أحد المسؤولين" بدلاً من اسم غير مؤكد، "عدد من الأشخاص" بدلاً من رقم غير مؤكد. الغموض المتعمد مقبول دائمًا؛ الحقيقة المختلقة غير مقبولة أبدًا.
- إذا لم تُزوَّد بعناوين أخبار لهذا الموضوع، التزم بوصف عام للموضوع دون افتراض أي سياق محدد (نوع حدث، تاريخ، مكان، أطراف) لم يُذكر صريحًا في الموضوع نفسه.
"""

MIN_TOTAL_WORDS = 280
MAX_TOTAL_WORDS = 420
MAX_ATTEMPTS = 3


def _total_narration_words(data: dict) -> int:
    return sum(len(s.get("narration", "").split()) for s in data.get("scenes", []))


def _request_script(messages: list[dict]) -> dict:
    response = client.chat.completions.create(
        model=SCRIPT_MODEL,
        messages=messages,
        response_format={"type": "json_object"},
        temperature=0.8,
    )
    return json.loads(response.choices[0].message.content)


CATEGORY_LABELS = {
    "incidents": "الحوادث والكوارث",
    "geopolitics": "الجيوسياسة والصراعات",
    "sports": "الرياضة الكبرى",
}


def generate_script(topic: str, context: list[str] | None = None, category: str | None = None) -> dict:
    cat_label = CATEGORY_LABELS.get(category or "", "")
    cat_note = f" (المحور: {cat_label})" if cat_label else ""
    user_prompt = f"اكتب سيناريو وثائقيًا إخباريًا بالعربية الفصحى حول الموضوع الرائج حاليًا: «{topic}»{cat_note}."
    if context:
        headlines = "\n".join(f"- {line}" for line in context)
        user_prompt += (
            "\n\nعناوين أخبار حقيقية مرتبطة بهذا الموضوع (استخدمها كمصدر وقائعي وحيد، ولا تخرج عنها):\n"
            f"{headlines}"
        )
    messages = [
        {"role": "system", "content": SYSTEM_PROMPT},
        {"role": "user", "content": user_prompt},
    ]

    data = None
    for attempt in range(MAX_ATTEMPTS):
        data = _request_script(messages)
        total_words = _total_narration_words(data)
        if MIN_TOTAL_WORDS <= total_words <= MAX_TOTAL_WORDS:
            break
        messages.append({"role": "assistant", "content": json.dumps(data, ensure_ascii=False)})
        if total_words < MIN_TOTAL_WORDS:
            messages.append({
                "role": "user",
                "content": (
                    f"عدد كلمات السرد الحالي هو {total_words} كلمة فقط، وهذا غير كافٍ، "
                    f"يجب أن يبلغ {MIN_TOTAL_WORDS} كلمة على الأقل ولا يتجاوز {MAX_TOTAL_WORDS} كلمة. "
                    f"أعد إخراج الـ JSON كاملاً مع توسيع narration في كل مشهد إلى 35-50 كلمة (جملة واحدة فقط لكل مشهد)، "
                    f"دون حذف أو تقصير أي مشهد، ودون إضافة مشاهد جديدة."
                ),
            })
        else:
            messages.append({
                "role": "user",
                "content": (
                    f"عدد كلمات السرد الحالي هو {total_words} كلمة، وهذا طويل جدًا ويتجاوز مدة الدقيقة الواحدة المسموحة لفيديو Shorts. "
                    f"أعد إخراج الـ JSON كاملاً مع تقليص narration في كل مشهد إلى 35-50 كلمة فقط (جملة واحدة لكل مشهد)، "
                    f"مع الحفاظ على 8 مشاهد بالضبط وعلى دقة الوقائع."
                ),
            })

    assert data is not None
    assert "scenes" in data and len(data["scenes"]) > 0, "Script generation returned no scenes"
    assert "title" in data and data["title"], "Script generation returned no title"

    return data


if __name__ == "__main__":
    import sys

    topic = sys.argv[1] if len(sys.argv) > 1 else "世宗大王与韩文的创制"
    script = generate_script(topic)
    print(json.dumps(script, ensure_ascii=False, indent=2))
